package ejercicio2.t4;

// Clase llamada "suma" que contiene dos métodos estáticos
public class suma {

    // Método estático que recibe dos números y devuelve su suma
    public static float suma(float n1, float n2) {
        return n1 + n2; // Retorna el resultado de la suma
    }

    // Método estático que recibe dos números y devuelve su producto
    public static float producto(float n1, float n2) {
        return n1 * n2; // Retorna el resultado de la multiplicación
    }
}
